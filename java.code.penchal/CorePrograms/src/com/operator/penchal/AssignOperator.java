package com.operator.penchal;

public class AssignOperator {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int a=5;
		int b=10;
		int c=15;
		a++;
		++a;
		
		
		
		b+=5;
		
		c-=10;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);

	}

}
