package com.string.penchal;

public class String_contains {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		String s1="Rama is good boy";
		System.out.println(s1.contains("sd"));
		System.out.println(s1.contains("is"));
		System.out.println(s1.contains("boy"));
	}

}
