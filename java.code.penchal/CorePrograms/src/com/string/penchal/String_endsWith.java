package com.string.penchal;

public class String_endsWith {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		String s1="World";
		System.out.println(s1.endsWith("d"));
		System.out.println(s1.startsWith("25"));
		System.out.println(s1.equalsIgnoreCase("world"));
		
	}

}
